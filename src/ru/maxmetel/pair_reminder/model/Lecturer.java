package ru.maxmetel.pair_reminder.model;

public class Lecturer {

	String chair;
	Integer chairOid;
	String fio;
	String shortFIO;
	Integer lecturerOid;

	public String getChair() {
		return chair;
	}
	public Integer getChairOid() {
		return chairOid;
	}
	public String getFio() {
		return fio;
	}
	public String getShortFIO() {
		return shortFIO;
	}
	public Integer getLecturerOid() {
		return lecturerOid;
	}
}
